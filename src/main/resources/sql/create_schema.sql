DROP TABLE IF EXISTS TXN_ONBOARD_ACCT;
CREATE TABLE IF NOT EXISTS txn_onboard_acct (
	ID VARCHAR(36) NOT NULL,
	OFFICER_ACCT_ID VARCHAR(36),
	FIRST_NAME VARCHAR(50),
	MIDDLE_NAME VARCHAR(50),
	LAST_NAME VARCHAR(50),
	STATUS VARCHAR(30),
	UPDATED_BY VARCHAR(36) DEFAULT '00000000000000000000000000000000' NOT NULL,
	UPDATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP ,
	CREATION_BY VARCHAR(36) DEFAULT '00000000000000000000000000000000' NOT NULL,
	CREATION_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP 
);

DROP TABLE IF EXISTS TXN_ONBOARD_FILE;
CREATE TABLE IF NOT EXISTS txn_onboard_file (
	ID VARCHAR(36) NOT NULL,
	OB_ACCT_ID VARCHAR(36) NOT NULL,
	FILE_ID VARCHAR(36),
	DOC_CODE VARCHAR(25),
	UPDATED_BY VARCHAR(36) DEFAULT '00000000000000000000000000000000' NOT NULL,
	UPDATED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP ,
	CREATION_BY VARCHAR(36) DEFAULT '00000000000000000000000000000000' NOT NULL,
	CREATION_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP 
);
